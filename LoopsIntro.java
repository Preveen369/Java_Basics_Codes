class LoopsIntro {
    public static void main(String[] args) {
        // loops repeats the statement multiple times
        // types: for, while, do-while loop

        int i = 1;
        // while loop
        while (i <= 6) {
            System.out.println("Happy Birthday!!");
            i++;
        }

        // for loop
        for (i = 1; i <= 6; i++) {
            System.out.println("Happy Birthday!!");
        }

        // do while loop
        do {
            System.out.println("Happy Birthday!!");
            i++;
        } while (i <= 6);

    }
}
