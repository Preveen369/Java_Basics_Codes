class RelationalOp {
    public static void main(String[] args) {
        // Relational Operators
        // <, >, <=, >=, ==, !=

        double a = 5.0450;
        double b = 5.0451;
        boolean result = a!=b;
        System.out.println(result);

        int num1 = 92;
        int num2 = 96;
        System.out.println(num1<num2);

    }
}
