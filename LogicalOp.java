class LogicalOp {
    public static void main(String[] args) {
        // Logical Operators
        // && -> AND, || -> OR, ! - NOT
        
        int x = 4;
        int y = 9;
        int a = 3;
        int b = 6;

        boolean result = x < y || a < b;
        System.out.println(!result);
    }
}
